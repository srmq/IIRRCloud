declare(strict_types = 1);

namespace iirrc\middlewares;

use Psr\Http\Message\ResponseInterface;
use Psr\Http\Message\ServerRequestInterface;
use Psr\Http\Server\MiddlewareInterface;
use Psr\Http\Server\RequestHandlerInterface;
use \DateTime;

class DateAdder implements MiddlewareInterface {
    /**
     * @var string The attribute name
     */
    private $attribute = 'request-date';

    /**
     * Set the attribute name to store the request date.
     */
    public function attribute(string $attribute): self
    {
        $this->attribute = $attribute;
        return $this;
    }

    /**
     * Process a server request and return a response.
     */
    public function process(ServerRequestInterface $request, RequestHandlerInterface $handler): ResponseInterface
    {
        $ts = new DateTime('UTC');
        return $handler->handle($request->withAttribute($this->attribute, $ts));
    }    


}

